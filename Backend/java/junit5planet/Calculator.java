public class Calculator {
	public int add(int a,int b) {
		return a+b;
		}

	public int multiply(int a,int b) {
		return a*b;
		
	}
	public int subtract(int a,int b) {
		return a-b;
	}

	public int division(int a,int b) {
		return a/b;
		
	}

	public int findLength(String a) {
		
		return a.length();
	}

}
